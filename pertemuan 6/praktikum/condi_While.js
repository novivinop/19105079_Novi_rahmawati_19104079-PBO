var noHari = 'RAB'

switch(noHari){
    case 'MIN' :
        console.log('Minggu')
        break
    case 'SEN' :
        console.log('Senin')
        break
    case 'SEL' :
        console.log('Selasa')
        break
    case 'RAB' :
        console.log('Rabu')
        break
    case 'KAM' :
        console.log('Kamis')
        break
    case 'JUM' :
        console.log('Jumat')
        break
    case 'SAB' :
        console.log('Sabtu')
        break
    default :
    console.log('tidak ada hari')
    break
}