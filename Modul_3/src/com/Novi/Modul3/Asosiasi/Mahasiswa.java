package com.Novi.Modul3.Asosiasi;

public class Mahasiswa {
    private int nim;
    private String nama;

    public Mahasiswa() {}

    public Mahasiswa(int nim, String nama){
        this.nim = nim;
        this.nama = nama;
    }

    public int getNim() {
        return nim;
    }
    public void setNim(int nim){
        this.nim = nim;
    }
    public void setNim(int nim){
        this.nim = nim;
    }
    public String getNama(){
        return nim;
    }
    public void setNama(String nama){
        this.nama = nama;
    }
}
